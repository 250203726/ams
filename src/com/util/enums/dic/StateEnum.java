package com.util.enums.dic;

/**
 * 是否启用
 * @author Wymann
 * @Date 2014-5-10 下午09:14:53
 *
 */
public enum StateEnum {
	INVALID("0"),	//停用
	VALID("1");	//启用
	
	private String value;
	private StateEnum(String value){
		this.value=value;
	}
	@Override
	public String toString(){
		return this.value;
	}
}
