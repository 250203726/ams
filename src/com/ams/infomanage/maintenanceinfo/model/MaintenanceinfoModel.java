package com.ams.infomanage.maintenanceinfo.model;

/**
 * 部门的视图实体
 * @author simon
 */
public class MaintenanceinfoModel {

	private String maintenanceId;		//主键
	private String maintenanceName;		//维护公司名称
	private String maintenanceContacts;		//维护公司联系人
	private String maintenanceContactsPhone;		//维护公司联系人电话
	
	public String getMaintenanceId() {
		return maintenanceId;
	}
	public void setMaintenanceId(String maintenanceId) {
		this.maintenanceId = maintenanceId;
	}
	public String getMaintenanceName() {
		return maintenanceName;
	}
	public void setMaintenanceName(String maintenanceName) {
		this.maintenanceName = maintenanceName;
	}
	public String getMaintenanceContacts() {
		return maintenanceContacts;
	}
	public void setMaintenanceContacts(String maintenanceContacts) {
		this.maintenanceContacts = maintenanceContacts;
	}
	public String getMaintenanceContactsPhone() {
		return maintenanceContactsPhone;
	}
	public void setMaintenanceContactsPhone(String maintenanceContactsPhone) {
		this.maintenanceContactsPhone = maintenanceContactsPhone;
	}

	

}
